package com.neugent.aethervoice.xml.parse;

public class DirectoryDataset {
	private String category, city, companyName, address, id, listingCode, subCategory, telephone, totalCount;
	
	
    public String getCategory() { return category; }    
    public void setCategory(String category) {
        this.category = category;
    }
    
    public String getCity() { return city; }    
    public void setCity(String city) {
        this.city = city;
    }
    
    public String getCompanyName() { return companyName; }    
    public void setCompanyName(String companyName) {
        this.companyName = companyName;
    }
    
    public String getAddress() { return address; }    
    public void setAddress(String address) {
        this.address = address;
    }
    
    public String getId() { return id; }    
    public void setId(String id) {
        this.id = id;
    }
    
    public String getListingCode() { return listingCode; }    
    public void setListingCode(String listingCode) {
        this.listingCode = listingCode;
    }
    
    public String getSubCategory() { return subCategory; }    
    public void setSubCategory(String subCategory) {
        this.subCategory = subCategory;
    }
    
    public String getTelephone() { return telephone; }    
    public void setTelephone(String telephone) {
        this.telephone = telephone;
    }
    

    
}
