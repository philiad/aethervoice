package com.neugent.aethervoice.xml.parse;

public class BannerDataset {
	private String bannerid, description, filename, image_url, page_url;
	
	
    public String getBannerid() { return bannerid; }    
    public void setBannerid(String bannerid) {
        this.bannerid = bannerid;
    }
    
    public String getDescription() { return description; }    
    public void setDescription(String description) {
        this.description = description;
    }
    
    public String getFilename() { return filename; }    
    public void setFilename(String filename) {
        this.filename = filename;
    }
    
    public String getImage_url() { return image_url; }    
    public void setImage_url(String image_url) {
        this.image_url = image_url;
    }
    
    public String getPage_url() { return page_url; }    
    public void setPage_url(String page_url) {
        this.page_url = page_url;
    }
    
    
}
